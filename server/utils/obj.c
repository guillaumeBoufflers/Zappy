#include		"server.h"

char			*obj_to_str(t_object_types o)
{
  if (o == FOOD)
    return ("nourriture");
  else if (o == LINEMATE)
    return ("linemate");
  else if (o == DERAUMERE)
    return ("deraumere");
  else if (o == SIBUR)
    return ("sibur");
  else if (o == MENDIANE)
    return ("mendiane");
  else if (o == PHIRAS)
    return ("phiras");
  else if (o == THYSTAME)
    return ("thystame");
  else
    return ("joueur");
}
